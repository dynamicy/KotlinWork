
/**
 * Math Operation
 */
fun main(args: Array<String>) {

    var n1: Int = readLine()!!.toInt()
    var n2: Int = readLine()!!.toInt()
    var result: Int

    result = n1 + n2

    println("result: " + result)

    result = n1 - n2

    println("result: " + result)

    result = n1 * n2

    println("result: " + result)

    result = n1 / n2

    println("result: " + result)
}