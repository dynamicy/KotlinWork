fun main(args: Array<String>) {

    val demoStr = " this is an apple. "

    // trim()
    println(demoStr.trim())

    // split
    println(demoStr.split(" "))

    // toLowerCase
    println(demoStr.toLowerCase())

    // toUpperCase
    println(demoStr.toUpperCase())

    // plus
    println(demoStr.plus("banana"))

    // replace
    println(demoStr.replace(" ", "-"))
}